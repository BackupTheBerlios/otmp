<h3>1&nbsp;&nbsp;&nbsp;&nbsp;Was ist OTMP</h3>

<p>&Uuml;bersetzt man den Namen Online-Translation-Market-Place kurz <b>OTMP</b>,
so wird eine guter ungef&auml;hrer Eindruck vermittelt, um was es sich hierbei handet:
Einem Online-Sprach-Markt-Platz.</p>

<p>Diese &Uuml;bersetzung verhilft hier zwar sich eine grobe Vorstellung zu schaffen,
reicht jedoch niemals aus, um das ganze Projekt voll und ganz zu verstehen. Daher
versuchen wir bassierend auf den gegebenen Vorgaben das Projekt genauer zu umreiﬂen.</p>

<p>Bei <b>OTMP</b> handelt es sich um ein Projekt, dass zum &Uuml;bersetzen von wissenschaftlichen
Texten, in welche Kategorie auch immer, erdacht wurde. Das ist aber nur ein Nebenergebnis.
Haupts&auml;chlich dient es dazu Sprachen zu konservieren die vom Aussterben bedroht sind.</p>

<p>Ein Drittel der ca. 6500 Sprachen, die zur Zeit weltweit gesprochen werden, wird innerhalb der n&auml;chsten Jahre und Jahrzehnte aussterben. Pessimistische Sch&auml;tzungen gehen sogar davon aus, dass im Laufe dieses jahrhunderts bis zu 90% der jetzt noch gesprochenen Sprachen aussterben werden. Die meisten dieser vom aussterben bedrohten Sprachen befinden sich in den heutigen Entwicklungsl&auml;nder.</p>

<p>Ein groﬂes Problem hier sind die fehlenden Hilfsmittel und Werkzeuge, um den in den L&auml;nder lebenden Menschen zu erm&ouml;glichen, ihre Dialekte/Sprache vor dem Aussterben zu retten. Besonders weil die meisten Menschen in diesen Regionen andere Probleme haben. <b>OTMP</b> bietet eine M&ouml;glichkeit f&uuml;r diese Menschen, ihre Sprache bzw. ein Teil davon z.B. in Form eines Textes, schriftlich zu erfassen, um es dann international ver&ouml;ffentlichen zu k&ouml;nnen. Das System stellt dann auch noch &Uuml;bersetzungsm&ouml;glichkeiten zur Verf&uuml;gung um die Sprache nicht nur zu konservieren, sondern auch noch deren Bedeutung festhalten zu k&ouml;nnen.</p>

<p>Nun ist <b>OTMP</b> nicht das einzige System mit dem Angebot Texte in andere Sprachen &uuml;bersetzen zu lassen, aber vermutlich das einzige, welches dabei versucht Sprachen zu konservieren und vieleicht so auch zu retten, besonders die gef&auml;hrdeten Sprachen in den Entwicklungsl&auml;ndern.</p>

<p>Das <b>OTMP</b>-System basiert komplett auf Open Source Werkzeugen und ist so unabh&auml;gig von Lizenzen und Geb&uuml;hren einsetzbar. Aber es hat noch andere Vorteile f&uuml;r das Projekt:</p>

<ol>
<li>Auch in den <i>&auml;rmsten</i> L&auml;ndern k&ouml;nnen <i>arme</i> Menschen angesprochen werden, Das System ist sehr unaufwendig und kann auf minimalsten Ger&auml;ten laufen, welche diesen L&auml;ndern gestellt werden k&ouml;nnen,
<li>Menschen die ihre Zeit nicht besser zu nutzen wissen, k&ouml;nnen f&uuml;r dieses Projekt geworben werden um &Uuml;bersetzungen anzufertigen.
<li>Orginal-Texte in der Orginal-Sprache k&ouml;nnen weltweit ver&ouml;ffentlicht werden, ohne das andere Benutzer davon ausgeschlossen werden und
<li>Auch in den Inhalt allen Menschen in der Welt Zug&auml;gnlich zu machen durch &Uuml;bersetzungen.
</ol>

<p><b>OTMP</b> erhofft sich damit eine interkulturelle Verbindung auf der geistigen Ebene der Kreativit&auml;t, Intelligenz, Kultur und Sprache aufzubauen. Somit ist es auch klar, dass niemanden auf Grund seiner Herkunft ausgeschlossen wird. Jeder, der an den Projekt helfen m&ouml;chte, kann sich als benutzer am System anmelden, wo immer er/sie auch herkommt. Damit auch jeder gleichberechtigt ist, will <b>OTMP</b> besonders die Gruppe von Menschen f&ouml;rdern und unterst&uuml;tzen, die von vornherein benachteiligt sind, das sind in erster Linie die in den Entwicklungsl&auml;nder lebenden Menschen.</p>

<p><b>OTMP</b> ist somit ein System, dass sich zum Ziel gesetzt hat, diesen Sprach-Markt-Platz mit dem groﬂen Schwerpunkt Entwicklungsl&auml;nder zu erschaffen.<p>

<p><b>OTMP</b> besteht also aus eine Vielfalt von User/Menschen, die nur einen Ziel verfolgen, n&auml;mlich ihren wissenschaftlichen Text, entweder zu ver&ouml;ffentlichen (Online zu stellen), oder ihn zum &Uuml;bersetzen "frei" geben, und Personen die ihre Texte &uuml;bersetzen um ihre Sprache zu konservieren!</p>

<p>Beim Entwickeln des gesamten Projekts wurden einige Punkte zu Barrieren! Sie erschweren uns Entwickler unsere Idealvorstellung des Projekts durchzusetzten! Unter anderen w&auml;ren da folgende Punkte:</p>

<ol>
<li>Die Finanzierung des Gesamten Projekts/System
<li>Lizensierung
<li>nicht f&uuml;r jede der Sprachen existieren die uni-codes d.h auch Sprachen ohene Schriftbild
</ol>

<p>Auch f&uuml;r jede dieser Punkte wurden einige L&ouml;sungsvorschl&auml;ge und Ans&auml;tze erdacht bzw. werden vorgeschlagen!</p>

