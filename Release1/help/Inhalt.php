Zielgruppe

Das OTMP-System hat speziell zwei große Zielgruppen.

Erstens Menschen in aller Welt die Zugang zum Internet haben,
mehrere Sprachen beherschen und viel Zeit haben und diese nicht
zu füllen wissen. Besonders ältere Menschen fallen in diese
Zielgruppe. Einige dieser Menschen könnten durch die Arbeit einen
sinnvollen Zeitvertreib erkennen. Besonders Menschen, welche
Sprachen beherrschen, die nur von wenigen Menschen gesprochen wird
oder vom Aussterben bedroht ist.

Zweitens Menschen welche interessante und wichtige Dokumente
erstellt haben und diese in anderen, besonders weniger verbreiteten,
Sprachen weitergeben wollen, ohne diese Sprache zu beherrschen.


Systembeschreibung

